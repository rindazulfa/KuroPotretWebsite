     */
    public function store(Request $request)
    {
        DB::table('packages')->insert([
            //'id' => $request->id,
            'name_pack' => $request->name,
            'price' => $request->harga,
            'qty_photos' => $request->jumlah_foto,
            'qty_edit' => $request->jumlah_edit,
            'duration' => $request->durasi_video,
            'working_hours' => $request->durasi_sewa,
            'price_operational' => $request->biaya_ops
        ]);

        return redirect('pages.admin.product');
    }

